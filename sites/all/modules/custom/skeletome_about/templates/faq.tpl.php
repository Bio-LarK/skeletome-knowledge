                <p>To make a contribution (e.g. add or discuss a Statement), you need to be logged in to your account.</p>
            </div>
            <div class="section-segment">
                <h3>How can I register for an account?</h3>
                <p>To register for an account, click the Register button in the top right corner and follow the instructions.</p>
            </div>

            <div class="section-segment">
                <h3>How long does the approval process take?</h3>
                <p>Your registration will be reviewed and your approval will be sent to your email as soon as possible. If you
                    feel there is a problem, please email us at knowledge@skeletome.org</p>
            </div>

            <div class="section-segment">
                <h3>How do I log in?</h3>
                <p>To log in, click the 'Login' button in the top right corner and follow the instructions.</p>
            </div>

            <div class="section-segment">
                <h3>How do I become an editor?</h3>
                <p>Editors are allowed to edit and change the abstract and add x-rays for bone dysplasias and genes. If you would
                    like to become an editor, please knowledge@skeletome.org</p>
            </div>

            <div class="section-segment">
                <h3>How do I become a curator?</h3>
                <p>Curators are responsible for maintaining the integrity of a page.
                    They can add new clinical features or genes to a bone dypslasia.
                    If you would like to become an editor, please knowledge@skeletome.org</p>
            </div>

            <div class="section-segment">
                <h3>Who wrote an abstract?</h3>
                <p>Abstracts are collaboratively written by members of the skeletal dysplasia
                    community. To ensure the highest quality, only expert users will be given editing rights for the abstracts.
                    The names of the contributors are listed above the abstract.</p>
            </div>

            <div class="section-segment">
                <h3>How can I view a contributors profile page?</h3>
                <p>Click the name of a contributor above the abstract or search for their username in the search bar.</p>
            </div>

            <div class="section-segment">
                <h3>How can make a statement?</h3>
                <p>Any logged-in user can create a Statement.</p>
            </div>
        </section>
    </div>
    <div class="span6">
        <section>
            <div class="section-segment">
                <h3>What information should be expressed in a statement?</h3>
                <p>Statements can be important clinical observations, pathophysiological insights etc</p>
            </div>

            <div class="section-segment">
                <h3>Who can contribute X-Rays?</h3>
                <p>To ensure the highest quality, only expert users with editing rights can contribute x-rays.</p>
            </div>

            <div class="section-segment">
                <h3>How do I see a larger version of an X-Ray?</h3>
                <p>Click on a thumbnail to see a larger version of the x-ray. Click the small arrows on the
                    images to move back and forth through the series of images.</p>
            </div>

            <div class="section-segment">
                <h3>Where do they clinical features come from?</h3>
                <p>The original list was sourced from OMIM, but can be edited by the skeletal dysplasia community. The clinical
                    feature vocabulary is based on the [Human Phenotype Ontology].</p>
            </div>

            <div class="section-segment">
                <h3>Who can edit the clinical features?</h3>
                <p>To ensure the highest quality, only expert users with editing rights can add or delete a clinical feature.</p>
            </div>

            <div class="section-segment">
                <h3>What information can I import into my Skeletome Profile</h3>
                <p>You can enhance your profile page by providing a biography and list of your publications which
                    can be imported from LinkedIn or Orcid.</p>
            </div>

            <div class="section-segment">
                <h3>How do I view the Bone Dysplasia Onotology?</h3>
                <p>Click the "Browse" button on the top of any page to browse the Skeletome Encyclopedia. Disorders are grouped according to the 2010
                    Nosology of The International Skeletal Dysplasia Society (Warman et al. 2011).</p>
            </div>

            <div class="section-segment">
                <h3>How do I see what bone dysplasias are in a group?</h3>
                <p>Navigate to the BDO page and click the group name. This reveals the disorders within this group,
                    clicking on a disease name takes you to the full entry for this disorder.</p>
            </div>

            <div class="section-segment">
                <h3>How do I find a bone dysplasia?</h3>
                <p>Start typing your query in the search box. Skeletome will display matching groups, diseases, genes or clinical
                    features below the search box. Select one of the suggestions or hit 'Enter' to start a full text search across
                    all entries based on your query.</p>
            </div>

            <div class="section-segment">
                <h3>How do I filter my search results?</h3>
                <p>Search results can be further refined through filtering. This is particularly useful when searching for
                    clinical features. For example, you could search for all bone dysplasias with short long bones, and
                    then filter the results to only show the bone dysplasias that also have platyspondyly.</p>
            </div>
        </section>
